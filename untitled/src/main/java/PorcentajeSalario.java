public class PorcentajeSalario {
    // Atributos
    private float porcentajeSalarioArq;
    private float porcentajeSalarioDel;
    private float porcentajeSalarioMedioCam;
    private float porcentajeSalarioDef;


    // Constructor
    public PorcentajeSalario() {
        this.porcentajeSalarioArq = 0;
        this.porcentajeSalarioDel = 0;
        this.porcentajeSalarioDef = 0;
        this.porcentajeSalarioMedioCam = 0;
    }

    // Getters y Setters
    public float getPorcentajeSalarioArq() {
        return porcentajeSalarioArq;
    }

    public void setPorcentajeSalarioArq(float porcentajeSalarioArq) {
        this.porcentajeSalarioArq = porcentajeSalarioArq;
    }

    public float getPorcentajeSalarioDel() {
        return porcentajeSalarioDel;
    }

    public void setPorcentajeSalarioDel(float porcentajeSalarioDel) {
        this.porcentajeSalarioDel = porcentajeSalarioDel;
    }

    public float getPorcentajeSalarioMedioCam() {
        return porcentajeSalarioMedioCam;
    }

    public void setPorcentajeSalarioMedioCam(float porcentajeSalarioMedioCam) {
        this.porcentajeSalarioMedioCam = porcentajeSalarioMedioCam;
    }

    public float getPorcentajeSalarioDef() {
        return porcentajeSalarioDef;
    }

    public void setPorcentajeSalarioDef(float porcentajeSalarioDef) {
        this.porcentajeSalarioDef = porcentajeSalarioDef;
    }


}
